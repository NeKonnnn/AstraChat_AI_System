"""
Единые инструкции для ответов по контексту документов (ru/en).

Политика по умолчанию — мягкий RAG:
  - если в CONTEXT есть хоть какие-то зацепки, модель ОБЯЗАНА дать ответ,
    собирая максимум из найденных фрагментов и честно помечая, чего в тексте нет;
  - каноническая фраза «не нашёл» выводится ТОЛЬКО когда CONTEXT буквально пуст
    (этот случай обрабатывается на стороне backend: до вызова LLM).

Жёсткий режим (старое поведение) можно вернуть переменной окружения
RAG_STRICT_ANSWER_MODE=strict — модель будет отвечать канонической фразой, как только
нет прямой опоры, даже если есть косвенные зацепки.
"""

from __future__ import annotations

import os
from typing import Optional

RAG_STRICT_NOT_FOUND_MESSAGE = (
    "Извините, не нашёл ничего в базе документов по вашему запросу. "
    "Попробуйте переформулировать вопрос или проверьте, что нужные файлы загружены."
)

RAG_NO_EVIDENCE_DETAIL = (
    "Ответ без опоры на загруженные файлы я не формирую, чтобы не выдумывать факты."
)


def _strict_answer_mode() -> str:
    """soft (по умолчанию) | strict.

    soft  — LLM всегда пытается ответить, если в CONTEXT есть хоть какой-то материал,
            и честно указывает, чего не хватает.
    strict — LLM выводит каноническую фразу «не нашёл», если прямой опоры нет.
    """
    v = (os.environ.get("RAG_STRICT_ANSWER_MODE") or "soft").strip().lower()
    return "strict" if v in ("strict", "1", "true", "yes", "on", "hard") else "soft"


SOFT_CONTEXT_RULES = (
    "Правила ответа по CONTEXT (мягкий RAG):\n"
    "- Главный приоритет — помочь пользователю. Если в CONTEXT есть ХОТЬ КАКИЕ-ТО "
    "зацепки по сути вопроса (определения, смежные факты, упоминания), дай содержательный "
    "ответ на основе этих фрагментов. Не отказывайся, пока в CONTEXT вообще есть текст.\n"
    "- Объединяй факты из нескольких фрагментов в связный ответ. Допускаются разумные "
    "обобщения и перефраз, но без выдумывания конкретики (имён, дат, чисел), которых нет в CONTEXT.\n"
    "- Если ответ получается частичным — дай, что есть, и ОТДЕЛЬНЫМ предложением укажи, "
    "чего именно в найденных фрагментах не хватает (например: «в загруженных документах "
    "не нашёл точной даты»).\n"
    "- Если CONTEXT ПУСТОЙ или полностью НЕ РЕЛЕВАНТЕН теме (там действительно нет ни "
    "прямых, ни косвенных зацепок), тогда — и только тогда — скажи ТОЛЬКО следующий текст:\n"
    f"{RAG_STRICT_NOT_FOUND_MESSAGE}\n"
    "- По возможности помечай источник фактов: [имя_файла, чанк N].")


STRICT_CONTEXT_RULES_LEGACY = (
    "Правила ответа (строгий RAG):\n"
    "- Используй только предоставленный контекст (CONTEXT). Не опирайся на общие знания, если факта нет в CONTEXT.\n"
    "- Если в CONTEXT есть **частичный** ответ — дай его чётко; явно укажи, чего в тексте нет. "
    "Объединяй факты из **нескольких** фрагментов в связный ответ, если они относятся к вопросу.\n"
    f"- Если в CONTEXT **совсем нет** материала по сути вопроса (ни прямых, ни косвенных зацепок), скажи ТОЛЬКО следующий текст целиком:\n{RAG_STRICT_NOT_FOUND_MESSAGE}\n"
    "- Не выдумывай детали (имена, цифры, даты), которых нет в CONTEXT.\n"
    "- По возможности указывай источник для фактов: [имя_файла, чанк N]. Если источник неочевиден — формулируй осторожно."
)


STRICT_CONTEXT_RULES = SOFT_CONTEXT_RULES

def _active_rules() -> str:
    return STRICT_CONTEXT_RULES_LEGACY if _strict_answer_mode() == "strict" else SOFT_CONTEXT_RULES


def merge_strict_rag_system_prompt(existing: Optional[str]) -> str:
    """Добавляет правила CONTEXT к системному промпту при RAG.

    Название сохранено для обратной совместимости; фактически добавляются мягкие
    правила (по умолчанию) либо строгие при RAG_STRICT_ANSWER_MODE=strict.
    """
    block = build_rag_user_instruction()
    if not existing or not str(existing).strip():
        return block
    ex = str(existing).strip()
    if "только предоставленный контекст (CONTEXT)" in ex or "Правила ответа по CONTEXT" in ex:
        return ex
    return f"{ex}\n\n{block}"


def build_rag_user_instruction(with_citations: bool = True) -> str:
    base = _active_rules()
    if with_citations:
        return base
    # Убираем пункт про цитаты — он есть и в soft, и в strict.
    return (
        base.replace(
            "- По возможности помечай источник фактов: [имя_файла, чанк N].",
            "",
        )
        .replace(
            "- По возможности указывай источник для фактов: [имя_файла, чанк N]. Если источник неочевиден — формулируй осторожно.",
            "",
        )
        .strip()
    )
